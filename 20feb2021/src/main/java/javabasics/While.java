package javabasics;

public class While {

	public static void main(String[] args) {

		int numero = 95;
		
		while(numero<=100) {
			System.out.println(numero);
			// el ++ incrementa en 1 en automatico
			numero++;
		}
		
		for(int num=1 ; num<=100; num++) {
			System.out.println(num);
		}

	}

}
